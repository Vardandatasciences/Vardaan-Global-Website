// How to Run and Test the GetInTouch Component
//
// 1. Make sure you have the required dependencies:
//    - Check if you have react-router-dom installed. If not, run:
//      npm install react-router-dom
//
// 2. Start the development server:
//      npm start
//
// 3. Navigate to the "Get in touch" page:
//    - Click on the "Get in touch" button in the navigation bar, or
//    - Go to http://localhost:3000/get-in-touch
//
// 4. Test the Country Selection:
//    - Click on different country flags/names to see the address and map update dynamically
//
// 5. Test the Form:
//    - Try submitting the form with and without filling the required fields
//    - Check that the form validation works as expected
//
// 6. Test Responsive Design:
//    - Use browser dev tools to test different screen sizes
//    - Check that the mobile view activates at 703px width and below
//
// 7. Additional Features:
//    - The component saves the selected country in localStorage
//    - Try refreshing the page to see if your country selection persists
//    - Test the back-to-top arrow functionality
//
